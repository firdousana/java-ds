public class NumPattern {
    public static void main(String args[]){
        int n=5;
        for(int i=1;i<=n;i++){
            for(int j=1;j<=i;j++){
                System.out.print(j+"  ");//print j i.e column coz thats the column number we want as output
            }
            System.out.println();
        }
        
    }
    
}
